export type Expression = 'happy' | 'sad' | 'angry' | 'thinking' | 'neutral'
export type Language = 'hi' | 'en' | 'hinglish'

export interface Choice {
  id: string
  text_hi: string
  text_en: string
  text_hinglish: string
  outcome: {
    health?: number
    wisdom?: number
  }
  feedback_hi: string
  feedback_en: string
  feedback_hinglish: string
  isGood: boolean
  explanation_hi?: string
  explanation_en?: string
  explanation_hinglish?: string
  wrongExpression?: Expression
}

export interface Challenge {
  id: string
  title_hi: string
  title_en: string
  title_hinglish: string
  // Position in realm world (percent 0-100)
  mapX: number
  mapY: number
  // Guide dialogue when player reaches this challenge
  guideIntro_hi: string
  guideIntro_en: string
  guideIntro_hinglish: string
  // The story/scenario
  scenario_hi: string
  scenario_en: string
  scenario_hinglish: string
  narrator_hi?: string
  narrator_en?: string
  narrator_hinglish?: string
  choices: Choice[]
}

export interface Protagonist {
  name: string
  name_en: string
  role_hi: string
  role_en: string
  role_hinglish: string
  image: string
  accentColor: string
  // Guide voicelines
  greeting_hi: string
  greeting_en: string
  greeting_hinglish: string
  moveInstruction_hi: string
  moveInstruction_en: string
  moveInstruction_hinglish: string
  completionMsg_hi: string
  completionMsg_en: string
  completionMsg_hinglish: string
}

export interface Realm {
  id: string
  title_hi: string
  title_en: string
  title_hinglish: string
  subtitle_hi: string
  subtitle_en: string
  subtitle_hinglish: string
  color: string
  bgGradient: string
  bgImage: string
  intro_hi: string
  intro_en: string
  intro_hinglish: string
  protagonist: Protagonist
  mapGateX: number
  mapGateY: number
  challenges: Challenge[]
}

